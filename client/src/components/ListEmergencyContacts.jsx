// Helper function to get contacts by city
export const getContactsByCity = (city) => {
  return emergencyContacts.filter(contact => 
    contact.city.toLowerCase().includes(city.toLowerCase()) || 
    contact.city === "Provincial" || 
    contact.city === "National"
  );
};

// Helper function to get contacts by priority
export const getContactsByPriority = (priority) => {
  return emergencyContacts.filter(contact => contact.priority === priority);
};

// Helper function to get critical contacts
export const getCriticalContacts = () => {
  return emergencyContacts.filter(contact => 
    contact.priority === "critical" || contact.priority === "high"
  ).sort((a, b) => {
    const priorityOrder = { "critical": 0, "high": 1 };
    return priorityOrder[a.priority] - priorityOrder[b.priority];
  });
};

// Helper function to get local MDRRMO contacts
export const getLocalMDRRMOContacts = () => {
  return emergencyContacts.filter(contact => 
    contact.organization.includes("MDRRMO") || contact.organization.includes("CDRRMO")
  );
};